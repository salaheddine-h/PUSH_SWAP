#include "libft.h"

int	ft_atoi(const char *nptr)
{
	size_t	i;
	int		signe;
	long	result;

	i = 0;
	signe = 1;
	result = 0;
	while (nptr[i] == 32 || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			signe *= -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		if((result * 10) > INT_MAX || (result * 10) < INT_MAX)
			result = (result * 10) + (nptr[i] - '0');
		i++;
	}
	
	return ((int)(signe * result));
}
